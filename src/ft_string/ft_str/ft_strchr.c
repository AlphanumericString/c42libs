#include <stddef.h>

char	*ft_strchr(const char *str, int c)
{
	char		*ret;
	int			i;
	const char	target = (char)c;

	ret = NULL;
	i = 0;
	while (str[i] && !ret)
	{
		if (str[i] == target)
			ret = (char *)str + i;
		i++;
	}
	if (!str[i] && target == 0)
		return ((char *)str + i);
	return ((char *)ret);
}
